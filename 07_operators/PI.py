# Personal Information Program

# Create variables to store personal information
name = "Alex Johnson"           # string
age = 25                        # integer  
height = 1.75                   # float (height in meters)
is_student = True               # boolean

# Print all variables in one line
print(f"Name: {name}, Age: {age}, Height: {height}m, Student: {is_student}")

# Alternative way to print in one line
print("Name:", name, "| Age:", age, "| Height:", height, "m | Student:", is_student)