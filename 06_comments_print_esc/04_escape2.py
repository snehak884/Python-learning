# \n → Newline
print("Hello\nWorld")
# Output:
# Hello
# World

# \t → Tab
print("Item\tPrice")
print("Pen\t$2")
print("Book\t$10")
# Output:
# Item    Price
# Pen     $2
# Book    $10

# \\ → Backslash
print("This is a backslash: \\")
# Output:
# This is a backslash: \

# \" → Double quote
print("She said, \"Python is awesome!\"")
# Output:
# She said, "Python is awesome!"

# \' → Single quote
print('It\'s a sunny day!')
# Output:
# It's a sunny day!


# Quick recap of uncommon ones:

# \r → carriage return (overwrite)

# \b → backspace

# \f → form feed

# \u / \U → Unicode characters

# \ooo / \xhh → Octal / Hex representation