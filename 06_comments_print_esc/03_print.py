# Using sep (separator)

# By default, print() separates multiple values with a space.

# You can change this using the sep parameter.

print("Python", "Java", "C++")
# Output: Python Java C++

print("Python", "Java", "C++", sep=", ")
# Output: Python, Java, C++

print("2025", "09", "18", sep="-")
# Output: 2025-09-18


print("Hello", end=" ")
print("World")
# Output: Hello World

print("Loading", end="...")
print("Done")
# Output: Loading...Done
